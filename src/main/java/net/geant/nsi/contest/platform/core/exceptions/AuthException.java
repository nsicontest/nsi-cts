package net.geant.nsi.contest.platform.core.exceptions;

public class AuthException extends CTSException {

	public AuthException() {
		super();
	}

	public AuthException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AuthException(String arg0) {
		super(arg0);
	}

	public AuthException(Throwable arg0) {
		super(arg0);
	}

}
