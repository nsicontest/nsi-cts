package net.geant.nsi.contest.platform.core.exceptions;

public class CTSException extends Exception {

	public CTSException() {
		super();
	}

	public CTSException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CTSException(String arg0) {
		super(arg0);
	}

	public CTSException(Throwable arg0) {
		super(arg0);
	}

}
