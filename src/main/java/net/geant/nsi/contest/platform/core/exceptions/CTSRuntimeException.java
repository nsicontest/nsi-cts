package net.geant.nsi.contest.platform.core.exceptions;

public class CTSRuntimeException extends RuntimeException {

	public CTSRuntimeException() {
		super();
	}

	public CTSRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CTSRuntimeException(String arg0) {
		super(arg0);
	}

	public CTSRuntimeException(Throwable arg0) {
		super(arg0);
	}

}
